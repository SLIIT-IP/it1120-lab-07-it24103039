public class IT24103039Lab7Q2C   {
    public static void main(String[] args)	{

	//Defining variables 
	int rowCounter,columnCounter;
	
	//Creating the nested loop with two for loops
	for(rowCounter=5;rowCounter>=1;rowCounter--)
	{
	    for(columnCounter=rowCounter;columnCounter>=1;columnCounter--)
	      {
		System.out.print(rowCounter );
		}
		
		System.out.println();
	}
    }
}